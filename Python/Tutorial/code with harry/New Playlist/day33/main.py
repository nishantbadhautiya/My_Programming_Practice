# Dictionaries are ordered collection of data items. They store multiple items in a single variable

info = {'name': 'Karan', 'age': 19, 'eligible': True}
print(info)
print(info['name'])
print(info['eligible'])
print(info.values())  #print all the values in the dictionary
print(info.keys())    #print all the keys in the dictionary 
print(info.items())   #print all the key-value pairs in the dictionary
